package com.casa.util.forms;

import java.util.Date;

public class BranchListForm {

	private String branchcode;
	private Date branchname;
	private Date branchaddress;
	
	public String getBranchcode() {
		return branchcode;
	}
	public void setBranchcode(String branchcode) {
		this.branchcode = branchcode;
	}
	public Date getBranchname() {
		return branchname;
	}
	public void setBranchname(Date branchname) {
		this.branchname = branchname;
	}
	public Date getBranchaddress() {
		return branchaddress;
	}
	public void setBranchaddress(Date branchaddress) {
		this.branchaddress = branchaddress;
	}
	
	
}

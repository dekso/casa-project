package com.etel.util.forms;

public class GenericForm {

	private String id;
	private String value;
	
	public String getId() {
		return id;
	}
	public String getValue() {
		return value;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setValue(String value) {
		this.value = value;
	}
}

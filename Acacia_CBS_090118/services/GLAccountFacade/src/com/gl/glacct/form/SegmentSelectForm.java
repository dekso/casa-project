package com.gl.glacct.form;

public class SegmentSelectForm {

	private String mgl;
	private int segno;
	private String seg;
	private String bstype;
	
	public String getMgl() {
		return mgl;
	}
	public void setMgl(String mgl) {
		this.mgl = mgl;
	}
	public int getSegno() {
		return segno;
	}
	public void setSegno(int segno) {
		this.segno = segno;
	}
	public String getSeg() {
		return seg;
	}
	public void setSeg(String seg) {
		this.seg = seg;
	}
	public String getBstype() {
		return bstype;
	}
	public void setBstype(String bstype) {
		this.bstype = bstype;
	}
}
